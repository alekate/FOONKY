using System;

namespace Unity.Services.Analytics
{
    /// <summary>
    /// Helper struct to handle arguments for recording an AdImpression event.
    /// </summary>
    [Obsolete("Please create an AdImpressionEvent and use RecordEvent(...) instead.")]
    public struct AdImpressionParameters
    {
        /// <summary>
        /// Whether the user watched the entire ad (COMPLETED), part of the ad (PARTIAL) or none of the ad (INCOMPLETE).
        /// Most applicable for interstitial ads which can be skipped by players
        /// </summary>
        public AdCompletionStatus AdCompletionStatus;

        /// <summary>
        /// The ad network that served the ad - ie, UNITY, IRONSOURCE.
        /// This value is often passed on a per-impression level by your mediation provider or ad network.
        /// </summary>
        public AdProvider AdProvider;

        /// <summary>
        /// The unique identifier for the placement where the Ad appeared as integrated into the game.
        /// </summary>
        public string PlacementID;

        /// <summary>
        /// The name of the placement as integrated in the game - for example, "Boosters" or "Daily Reward".
        /// This would correlate with the placement name configured in your ad network or mediation provider.
        /// </summary>
        public string PlacementName;

        /// <summary>
        /// Optional.
        /// Indicates what type of ad is shown - for example, REWARDED, INTERSTITIAL or BANNER.
        /// </summary>
        public AdPlacementType? PlacementType;

        /// <summary>
        /// Optional.
        /// eCPM ("effective Cost Per Mille" - the revenue generated by 1000 impressions) in US Dollars.
        /// This value is often passed on a per-impression level by your mediation provider or ad network and used in ad LTV calculations.
        /// </summary>
        public double? AdEcpmUsd;

        /// <summary>
        /// Optional.
        /// The store the player is directed to after clicking the ad - for example, Google Play, App Store, Amazon.
        /// </summary>
        public string AdStoreDstID;

        /// <summary>
        /// Optional.
        /// The Ad SDK version you are using.
        /// </summary>
        public string SdkVersion;

        /// <summary>
        /// Optional.
        /// </summary>
        public string AdImpressionID;

        /// <summary>
        /// Optional.
        /// </summary>
        public string AdMediaType;

        /// <summary>
        /// Optional.
        /// </summary>
        public Int64? AdTimeWatchedMs;

        /// <summary>
        /// Optional.
        /// </summary>
        public Int64? AdTimeCloseButtonShownMs;

        /// <summary>
        /// Optional.
        /// </summary>
        public Int64? AdLengthMs;

        /// <summary>
        /// Optional.
        /// </summary>
        public bool? AdHasClicked;

        /// <summary>
        /// Optional.
        /// </summary>
        public string AdSource;

        /// <summary>
        /// Optional.
        /// </summary>
        public string AdStatusCallback;
    }
}
